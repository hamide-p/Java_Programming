package java_selfpractice.day04_practices;

public class Square {

    public static void main(String[] args) {

        int side = 5;
        int areaOfSquare = side * side;
                int perimeterOfSquare = 4 * side;

        System.out.println("Area of the square is " + areaOfSquare);
        System.out.println("Perimeter of the square is " + perimeterOfSquare);

                //  (side) × (side) square units = area of square

                // (4 × side) units = perimeter of square

    }
}
