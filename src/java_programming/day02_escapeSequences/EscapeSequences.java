package java_programming.day02_escapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello students\nToday we have Java class");
        System.out.println("--------------------------------------------");
        System.out.println("\tJava Programming Language");
        System.out.println("\t\tjava Developer Course batch 1");
        System.out.println("-------------------------------------");
        System.out.println("\\");
        System.out.println(" ------------------------------------");
        System.out.println("My favourite Tv show is \"The Walking Dead\"");

        /*
        1. \n: New Line
        2. \t: paragraph space
        3. \\: Back slash
        4.\": double quote
         */






    }



}
