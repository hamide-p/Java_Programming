package day05_operators;

public class RelationalOperators2 {

    public static void main(String[] args) {

        // ==:

        System.out.println(1000 == 2000);

        System.out.println("Java" == "Java"); // java is equal to java

        System.out.println("java" == "Java"); // java is case sensitive "j" --> "J"

        System.out.println("Muhtar" == "Good Guy"); // they don't have the same string of type

        System.out.println( true == false ); // boolean expression "true $ false

        System.out.println(".................");

        // !=: not equal operator

        System.out.println(1000 != 2000); // true

        System.out.println("Java"!= "Java"); // false

        System.out.println("java" != "Java"); // true

        System.out.println("Muhtar" != "Good Guy"); // true

        System.out.println( true != false ); // true

        System.out.println(".................");








    }
}
