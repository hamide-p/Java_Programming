package java_live_review.week1;

public class Computer {

    public static void main(String[] args) {

        // declaring multiple variables
        String brand, processor;
        byte ram;
        int storage, numberOfUsb;
        boolean hasBluetooth, hasWifiCard;

        // assigning the variables
        brand = "HP";
        processor = "I7";
        ram = 8;
        storage = 500;
        numberOfUsb = 3;
        hasBluetooth = true;
        hasWifiCard = false;

        /*
            declaring & assigning



         */

        System.out.println("brand = " + brand);
        System.out.println("processor = " + processor);
        System.out.println("ram = " + ram);
        System.out.println("storage = " + storage);
        System.out.println("numberOfUsb = " + numberOfUsb);
        System.out.println("hasBluetooth = " + hasBluetooth);
        System.out.println("hasWifiCard = " + hasWifiCard);


    }

}



